# Aula 2.2. Tipos básicos: números, textos (strings), booleanos.

# Textos (strings)
texto = "Olá, mundo!"

print(texto)
print(type(texto)) 
print("="*16 + "\n") 

# Números
numero_inteiro = 10
numero_decimal = 10.5

print(numero_inteiro)
print(type(numero_inteiro))
print("="*16 + "\n") 

print(numero_decimal)
print(type(numero_decimal))
print("="*16 + "\n") 

# Booleanos
verdadeiro = True
print(verdadeiro)
print(type(verdadeiro))
print("="*16 + "\n") 

falso = False
print(falso)
print(type(falso))