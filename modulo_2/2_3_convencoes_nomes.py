# Aula 2.3. Convenções de nomes de variáveis.

# Nomes de variáveis devem ser descritivos e seguir algumas convenções:
# - Usar letras minúsculas e separar palavras com underscores (ex: nome_completo)
# - Evitar nomes genéricos como "var" ou "temp"
# - Usar nomes que indiquem o conteúdo ou a finalidade da variável
# - Não podem iniciar com números
# - Não usar palavras reservadas do Python (ex: if, for, while, etc.)

# Exemplos de nomes de variáveis válidas
nome_completo = "João da Silva"
idade = 30
altura = 1.75
possuiSaldo = True

print(nome_completo)
print(idade)
print(altura)
print(possuiSaldo)

# Exemplos de nomes de variáveis inválidas
# 1. 1idade = 30
# 2. for = "Python"
# 3. var = 10