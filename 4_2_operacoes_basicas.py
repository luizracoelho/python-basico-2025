# Aula 4.2. Operações básicas: soma, subtração, multiplicação, divisão.

a = 11
b = 6

# Soma
soma = a + b
print(f"Soma: {soma}")

# Subtração
subtracao = a - b
print(f"Subtração: {subtracao}")

# Multiplicação
multiplicacao = a * b
print(f"Multiplicação: {multiplicacao}")

# Divisão
divisao = a / b
print(f"Divisão: {divisao:.2f}") 

# Divisão: Obter número inteiro
divisao_inteira = a // b
print(f"Divisão Inteira: {divisao_inteira}")

# Divisão: Obter resto
divisao_resto = a % b
print(f"Divisão Resto: {divisao_resto}")

